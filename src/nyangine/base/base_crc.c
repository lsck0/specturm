u8 nya_crc8(const u8* data, u64 len) {
  nya_assert(data != nullptr);

  u8 crc = 0;
  for (u64 i = 0; i < len; i++) crc = CRC8_TABLE[crc ^ data[i]];

  return crc;
}

u16 nya_crc16(const u8* data, u64 len) {
  nya_assert(data != nullptr);

  // CCITT-FALSE
  u16 crc = 0xFFFF;
  for (u64 i = 0; i < len; i++) {
    crc ^= (u16)data[i] << 8;
    for (int j = 0; j < 8; j++) {
      if (crc & 0x8000) {
        crc = (crc << 1) ^ 0x1021;
      } else {
        crc <<= 1;
      }
    }
  }

  return crc;
}

u32 nya_crc32(const u8* data, u64 len) {
  nya_assert(data != nullptr);

  u32 crc = 0xFFFFFFFF;
  for (u64 i = 0; i < len; i++) crc = (crc >> 8) ^ CRC32_TABLE[(crc & 0xFF) ^ data[i]];

  return crc ^ 0xFFFFFFFF;
}

u64 nya_crc64(const u8* data, u64 len) {
  nya_assert(data != nullptr);

  u64 crc = 0xFFFFFFFFFFFFFFFFULL;
  for (u64 i = 0; i < len; i++) crc = (crc >> 8) ^ CRC64_TABLE[(crc & 0xFF) ^ data[i]];

  return crc ^ 0xFFFFFFFFFFFFFFFFULL;
}
